 

function fit = findFitness(xi,yi,c1,c2,cw,a1,a2,aor,a12,aw,b1,b2,k1,k2,kor,k12,kw,...
    s1,s2,gamma1,gamma2,delta)

fit = a1*c1(xi,yi)/(c1(xi,yi)+k1) + a2*c2(xi,yi)/(c2(xi,yi)+k2) +...
        aor*( (c1(xi,yi) + c2(xi,yi)) / (c1(xi,yi) + c2(xi,yi) + kor) ) +...
        (a12*c1(xi,yi)*c2(xi,yi))/(c1(xi,yi)*c2(xi,yi) + k12) -...
        aw*cw(xi,yi)/(cw(xi,yi)+kw) - b1*s1 - b2*s2 - ...
        gamma1*(s1>0) - gamma2*(s2>0) -...
        delta*sqrt(s1*s2);
end

